package controller;

/**
 * This is a controller class that manages arithmetic operations.
 * 
 * @author emalianakasmuri
 */
public class NumberCalculator {

	/**
	 * A method that calculates the sum of three numbers.
	 *
	 * @param number1
	 * @param number2
	 * @param number3
	 * @return the sum of three numbers
	 */
	public int numberSum(int number1, int number2, int number3) {
		return number1 + number2 + number3;
	}

	/**
	 * This method multiplies three numbers.
	 *
	 * @param number1
	 * @param number2
	 * @param number3
	 * @return the product of three numbers
	 */
	public int numberMultiplication(int number1, int number2, int number3) {
		return number1 * number2 * number3;
	}
}